
public class RelationalOperator {


public static void main(String[] args) {
	System.out.println(10==20);
	System.out.println(20>20);
	System.out.println(20>=20);
	System.out.println(10!=20);
	System.out.println(20==20 && 10>20);//since one of them is false it returs false bcz && accepts both condution to be true.
	System.out.println(20==20 || 10<20);//either any of this is true it returns true or else it returns false
}
}


