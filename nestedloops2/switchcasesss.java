package nestedloops2;

public class switchcasesss {
	public static void main(String args[]){
		char tech = 'a';
		switch(tech){
		case 'a':
		{
		System.out.println("java");
		break;
		}
		case 'b':{
		System.out.println("ES6");
		break;
		}
		default:{
		System.out.println("Not Listed..");
		}
		}
		}
}
